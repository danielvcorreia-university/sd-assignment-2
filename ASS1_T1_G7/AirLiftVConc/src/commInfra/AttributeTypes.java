package commInfra;

/**
 * Definition of possible attribute types exchange between the clients and the servers.
 */
public class AttributeTypes {
    /**
     * No parameter used or void return value.
     */
    public static final int NONE = 0;

    /**
     * Integer parameter or return value used.
     */
    public static final int INTEGER = 1;

    /**
     * Boolean parameter or return value used.
     */
    public static final int BOOLEAN = 2;
}
